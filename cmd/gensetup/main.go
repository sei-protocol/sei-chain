// Precompile version management tool, triggered via go generate.
//
// When a new version is added to app/tags, this tool:
//  1. Detects which precompile modules changed (via git)
//  2. Archives their current code into legacy/ folders
//  3. Regenerates all setup.go files from versions files
//
// Upgrade names use vMajor.Minor format (e.g. v7.0, v7.1).
// If vMajor.Minor.Patch is found in app/tags, patch is dropped with a warning
// and the file is rewritten.
// Legacy folder/package names use vMajorMinor0 form (v7.0 -> v700).
//
// Usage: go generate ./...
package main

import (
	"bufio"
	"embed"
	"fmt"
	"go/format"
	"io"
	"os"
	"path/filepath"
	"regexp"
	"strings"
	"text/template"

	"github.com/go-git/go-git/v5"
	"github.com/go-git/go-git/v5/plumbing/object"
	"github.com/go-git/go-git/v5/plumbing/storer"
	"golang.org/x/mod/semver"
)

//go:embed setup.go.tmpl
var tmplFS embed.FS

const (
	tagFile        = "app/tags"
	precompilesDir = "precompiles"
	commonDir      = "precompiles/common"
	modulePath     = "github.com/sei-protocol/sei-chain"
)

var (
	excludeDirs        = map[string]bool{"common": true, "utils": true}
	packageRe          = regexp.MustCompile(`^package\s+\S+`)
	commonImport       = `"` + modulePath + `/precompiles/common"`
	generatedHdr       = "// Code generated by cmd/gensetup; DO NOT EDIT."
	vMajorMinorRe      = regexp.MustCompile(`^v\d+\.\d+$`)
	vMajorMinorPatchRe = regexp.MustCompile(`^v\d+\.\d+\.\d+$`)
)

// --- Template types ---

type LegacyVersion struct {
	Tag    string
	Folder string
}

type TemplateData struct {
	PackageName    string
	LegacyVersions []LegacyVersion
}

func main() {
	// --- Phase 1: Read app/tags and detect new version ---
	tags, err := readNonEmptyLines(tagFile)
	if err != nil {
		fatalf("reading %s: %v", tagFile, err)
	}
	if len(tags) == 0 {
		fatalf("%s is empty", tagFile)
	}

	latestTag := tags[len(tags)-1]

	// --- Phase 2: Check if archiving is needed ---
	// Use versionFolder to compute the legacy dir for BOTH vMajor.Minor and
	// vMajor.Minor.Patch formats (versionFolder("v6.4.0") == versionFolder("v6.4") == "v640").
	tagFolder := versionFolder(latestTag)
	commonLegacyDir := filepath.Join(commonDir, "legacy", tagFolder)
	alreadyArchived := dirExists(commonLegacyDir)

	if !alreadyArchived {
		// New version detected — sanitize, validate, and archive.
		// Sanitization only applies to NEW entries; old tags are left as-is.
		sanitized := sanitizeUpgradeName(latestTag)
		if sanitized != latestTag {
			tags[len(tags)-1] = sanitized
			writeLines(tagFile, tags)
			fmt.Printf("rewrote %s in %s -> %s\n", latestTag, tagFile, sanitized)
			latestTag = sanitized
			tagFolder = versionFolder(latestTag)
		}
		archiveNewVersion(tags, latestTag, tagFolder)
	} else {
		fmt.Printf("version %s already archived, skipping to setup.go generation\n", latestTag)
	}

	// --- Phase 3: Always regenerate all setup.go files ---
	regenerateAllSetup()
}

// --- Archival orchestration ---

func archiveNewVersion(tags []string, newTag, tagFolder string) {
	fmt.Printf("new version detected: %s (folder: %s)\n", newTag, tagFolder)

	// Validate the new tag
	if pos, match := semverIndexOf(tags[:len(tags)-1], newTag); pos != -1 {
		fatalf("%s (semver-equal to %s) already exists in %s at line %d", newTag, match, tagFile, pos+1)
	}

	latestExisting := findLatestSemver(tags[:len(tags)-1])
	if latestExisting != "" && semver.Compare(newTag, latestExisting) < 0 {
		fatalf("%s is older than the current latest upgrade %s; upgrade names must advance forward", newTag, latestExisting)
	}

	// Open the git repository and find the commit where the previous version
	// was added to app/tags. We diff precompiles/ against that commit.
	repo, err := git.PlainOpenWithOptions(".", &git.PlainOpenOptions{DetectDotGit: true})
	if err != nil {
		fatalf("opening git repo: %v", err)
	}

	var baseCommit *object.Commit
	if latestExisting != "" {
		baseCommit = gitFindCommit(repo, latestExisting)
	}

	legacyCommonImport := `"` + modulePath + `/precompiles/common/legacy/` + tagFolder + `"`
	modules := discoverModules()

	// Detect which modules changed using git
	var changed []string
	commonChanged := false
	if baseCommit == nil {
		// First version ever — no previous commit to diff against, archive everything
		changed = modules
		commonChanged = true
	} else {
		fmt.Printf("comparing against %s (added %s)\n", baseCommit.Hash.String()[:12], latestExisting)
		if gitPathHasChanges(repo, baseCommit, commonDir) {
			commonChanged = true
		}
		for _, mod := range modules {
			moduleDir := filepath.Join(precompilesDir, mod)
			if commonChanged || gitPathHasChanges(repo, baseCommit, moduleDir) {
				changed = append(changed, mod)
			}
		}
	}

	if len(changed) == 0 {
		fmt.Println("no precompile changes detected, skipping archival")
		return
	}

	fmt.Printf("changed modules: %s\n", strings.Join(changed, ", "))

	// Archive common (always, when any module changed)
	archiveCommon(tagFolder)

	// Archive each changed module
	for _, mod := range changed {
		moduleDir := filepath.Join(precompilesDir, mod)
		archiveModule(moduleDir, mod, newTag, tagFolder, legacyCommonImport)
	}

	fmt.Println()
}

// --- Git helpers (go-git) ---

// gitFindCommit walks the commit log for app/tags and finds the commit
// that introduced the given version string (equivalent to git log -S).
func gitFindCommit(repo *git.Repository, version string) *object.Commit {
	head, err := repo.Head()
	if err != nil {
		fatalf("could not get HEAD: %v", err)
	}

	iter, err := repo.Log(&git.LogOptions{From: head.Hash()})
	if err != nil {
		fatalf("git log failed: %v", err)
	}
	defer iter.Close()

	// Walk commits that touched app/tags from newest to oldest.
	// Find the commit that INTRODUCED the version string: the newest commit
	// where the file contains the version, whose predecessor does not.
	var result *object.Commit
	var prev *object.Commit
	_ = iter.ForEach(func(c *object.Commit) error {
		file, err := c.File(tagFile)
		if err != nil {
			if prev != nil {
				result = prev
			}
			return storer.ErrStop
		}
		content, err := file.Contents()
		if err != nil || !strings.Contains(content, version) {
			if prev != nil {
				result = prev
			}
			return storer.ErrStop
		}
		prev = c
		return nil
	})
	if result == nil {
		result = prev
	}
	if result == nil {
		fatalf("could not find commit that introduced %s in %s; ensure your precompile changes are committed", version, tagFile)
	}
	return result
}

// gitPathHasChanges compares the HEAD tree against a base commit for a given
// directory prefix. Returns true if tracked files under that path differ.
func gitPathHasChanges(repo *git.Repository, base *object.Commit, dirPath string) bool {
	baseTree, err := base.Tree()
	if err != nil {
		fatalf("reading tree from base commit: %v", err)
	}

	baseSubtree, err := baseTree.Tree(dirPath)
	if err != nil {
		return true // path didn't exist at base — new module, treat as changed
	}

	headRef, err := repo.Head()
	if err != nil {
		fatalf("could not get HEAD: %v", err)
	}
	headCommit, err := repo.CommitObject(headRef.Hash())
	if err != nil {
		fatalf("reading HEAD commit: %v", err)
	}
	headTree, err := headCommit.Tree()
	if err != nil {
		fatalf("reading HEAD tree: %v", err)
	}
	headSubtree, err := headTree.Tree(dirPath)
	if err != nil {
		return true // path doesn't exist at HEAD — removed module, treat as changed
	}

	return baseSubtree.Hash != headSubtree.Hash
}

// --- Archival ---

func archiveCommon(tagFolder string) {
	targetDir := filepath.Join(commonDir, "legacy", tagFolder)
	if err := os.MkdirAll(targetDir, 0755); err != nil {
		fatalf("creating %s: %v", targetDir, err)
	}

	for _, name := range []string{"precompiles.go", "evm_events.go"} {
		src := filepath.Join(commonDir, name)
		if _, err := os.Stat(src); os.IsNotExist(err) {
			continue
		}
		dst := filepath.Join(targetDir, name)
		if err := copyAndRewritePackage(src, dst, tagFolder); err != nil {
			fatalf("archiving %s: %v", src, err)
		}
	}

	fmt.Printf("archived common -> legacy/%s/\n", tagFolder)
}

func archiveModule(moduleDir, moduleName, newTag, tagFolder, legacyCommonImport string) {
	targetDir := filepath.Join(moduleDir, "legacy", tagFolder)
	if err := os.MkdirAll(targetDir, 0755); err != nil {
		fatalf("creating %s: %v", targetDir, err)
	}

	entries, err := os.ReadDir(moduleDir)
	if err != nil {
		fatalf("reading %s: %v", moduleDir, err)
	}

	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		name := e.Name()
		if !strings.HasSuffix(name, ".go") || strings.HasSuffix(name, "_test.go") || name == "setup.go" {
			continue
		}

		src := filepath.Join(moduleDir, name)
		dst := filepath.Join(targetDir, name)
		if err := copyRewritePackageAndImports(src, dst, tagFolder, legacyCommonImport); err != nil {
			fatalf("archiving %s: %v", src, err)
		}
	}

	abiSrc := filepath.Join(moduleDir, "abi.json")
	if _, err := os.Stat(abiSrc); err == nil {
		if err := copyFile(abiSrc, filepath.Join(targetDir, "abi.json")); err != nil {
			fatalf("copying abi.json for %s: %v", moduleName, err)
		}
	}

	versionsFile := filepath.Join(moduleDir, "versions")
	if existing, err := readNonEmptyLines(versionsFile); err == nil {
		validateVersions(moduleName, existing)
		if pos, match := semverIndexOf(existing, newTag); pos != -1 {
			fatalf("%s/versions: %s (semver-equal to %s) already exists at line %d", moduleName, newTag, match, pos+1)
		}
	}
	appendLine(versionsFile, newTag)

	fmt.Printf("archived %s -> legacy/%s/\n", moduleName, tagFolder)
}

// --- Setup.go regeneration ---

func regenerateAllSetup() {
	tmpl, err := template.ParseFS(tmplFS, "setup.go.tmpl")
	if err != nil {
		fatalf("parsing template: %v", err)
	}

	entries, err := os.ReadDir(precompilesDir)
	if err != nil {
		fatalf("reading %s: %v", precompilesDir, err)
	}

	for _, entry := range entries {
		if !entry.IsDir() || excludeDirs[entry.Name()] {
			continue
		}

		moduleName := entry.Name()
		moduleDir := filepath.Join(precompilesDir, moduleName)

		versionsFile := filepath.Join(moduleDir, "versions")
		if _, err := os.Stat(versionsFile); os.IsNotExist(err) {
			continue
		}

		if _, err := os.Stat(filepath.Join(moduleDir, moduleName+".go")); os.IsNotExist(err) {
			fmt.Fprintf(os.Stderr, "skipping %s: no %s.go found\n", moduleName, moduleName)
			continue
		}

		versions, err := readNonEmptyLines(versionsFile)
		if err != nil {
			fatalf("reading versions for %s: %v", moduleName, err)
		}
		if len(versions) == 0 {
			continue
		}
		validateVersions(moduleName, versions)

		legacy := make([]LegacyVersion, 0, len(versions)-1)
		for _, v := range versions[:len(versions)-1] {
			legacy = append(legacy, LegacyVersion{
				Tag:    v,
				Folder: versionFolder(v),
			})
		}

		if err := generateSetup(tmpl, moduleDir, moduleName, legacy); err != nil {
			fatalf("generating setup.go for %s: %v", moduleName, err)
		}

		fmt.Printf("generated %s/setup.go (%d legacy versions)\n", moduleDir, len(legacy))
	}
}

func generateSetup(tmpl *template.Template, moduleDir, moduleName string, legacy []LegacyVersion) error {
	data := TemplateData{
		PackageName:    moduleName,
		LegacyVersions: legacy,
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("executing template: %w", err)
	}

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return fmt.Errorf("gofmt: %w\nraw:\n%s", err, buf.String())
	}

	return os.WriteFile(filepath.Join(moduleDir, "setup.go"), formatted, 0644)
}

// --- Validation ---

func sanitizeUpgradeName(input string) string {
	if vMajorMinorRe.MatchString(input) {
		return input
	}
	if vMajorMinorPatchRe.MatchString(input) {
		normalized := semver.MajorMinor(input)
		fmt.Fprintf(os.Stderr, "WARNING: dropping patch from %s — upgrade names are vMajor.Minor; using %s\n", input, normalized)
		return normalized
	}
	fatalf("upgrade name %q in %s is not valid; expected vMajor.Minor (e.g. v7.0, v7.1)", input, tagFile)
	return ""
}

func findLatestSemver(tags []string) string {
	latest := ""
	for _, t := range tags {
		if semver.IsValid(t) && (latest == "" || semver.Compare(t, latest) > 0) {
			latest = t
		}
	}
	return latest
}

func semverIndexOf(lines []string, value string) (int, string) {
	for i, l := range lines {
		if semver.Compare(l, value) == 0 {
			return i, l
		}
	}
	return -1, ""
}

// validateVersions ensures every entry in a module's versions file is a valid
// semver and that no two entries are semver-equal (e.g. v6.4 and v6.4.0).
func validateVersions(moduleName string, versions []string) {
	for i, v := range versions {
		if !semver.IsValid(v) {
			fatalf("%s/versions: line %d: %q is not a valid semver", moduleName, i+1, v)
		}
		if pos, match := semverIndexOf(versions[:i], v); pos != -1 {
			fatalf("%s/versions: line %d: %q is semver-equal to line %d (%q)", moduleName, i+1, v, pos+1, match)
		}
	}
}

// versionFolder converts a version tag to a folder/package name.
// Two-part versions (v7.0) are padded to three-part (v7.0.0) first,
// then dots are removed: v7.0 -> v700, v6.4.0 -> v640.
func versionFolder(v string) string {
	parts := strings.Split(strings.TrimPrefix(v, "v"), ".")
	if len(parts) == 2 {
		v = v + ".0"
	}
	return strings.ReplaceAll(v, ".", "")
}

func dirExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && info.IsDir()
}

func discoverModules() []string {
	entries, err := os.ReadDir(precompilesDir)
	if err != nil {
		fatalf("reading %s: %v", precompilesDir, err)
	}
	var modules []string
	for _, e := range entries {
		if e.IsDir() && !excludeDirs[e.Name()] {
			modules = append(modules, e.Name())
		}
	}
	return modules
}

// --- File rewriting ---

func copyAndRewritePackage(src, dst, newPkg string) error {
	content, err := os.ReadFile(src)
	if err != nil {
		return err
	}
	rewritten := packageRe.ReplaceAllString(string(content), generatedHdr+"\n\npackage "+newPkg)
	return os.WriteFile(dst, []byte(rewritten), 0644)
}

func copyRewritePackageAndImports(src, dst, newPkg, legacyCommonImport string) error {
	content, err := os.ReadFile(src)
	if err != nil {
		return err
	}
	rewritten := packageRe.ReplaceAllString(string(content), generatedHdr+"\n\npackage "+newPkg)
	rewritten = strings.ReplaceAll(rewritten, commonImport, legacyCommonImport)
	return os.WriteFile(dst, []byte(rewritten), 0644)
}

func copyFile(src, dst string) error {
	in, err := os.Open(src)
	if err != nil {
		return err
	}
	defer in.Close()

	out, err := os.Create(dst)
	if err != nil {
		return err
	}
	defer out.Close()

	_, err = io.Copy(out, in)
	return err
}

// --- File helpers ---

func appendLine(path, line string) {
	f, err := os.OpenFile(path, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0644)
	if err != nil {
		fatalf("opening %s: %v", path, err)
	}
	defer f.Close()
	fmt.Fprintln(f, line)
}

func readNonEmptyLines(path string) ([]string, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	var lines []string
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line != "" {
			lines = append(lines, line)
		}
	}
	return lines, scanner.Err()
}

func writeLines(path string, lines []string) {
	content := strings.Join(lines, "\n") + "\n"
	if err := os.WriteFile(path, []byte(content), 0644); err != nil {
		fatalf("writing %s: %v", path, err)
	}
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "gensetup: "+format+"\n", args...)
	os.Exit(1)
}
