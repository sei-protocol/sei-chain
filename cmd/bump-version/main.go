// Archives current precompile code into legacy/ folders and updates version
// tracking files. This replaces the old scripts/bump-version.sh.
//
// Upgrade names use vMajor.Minor format (e.g. v7.0, v7.1, v8.0).
// If vMajor.Minor.Patch is given, the patch is dropped with a warning.
// Precompile legacy folders use the dots-removed form (v7.0 -> v70).
//
// Usage:
//
//	go run ./cmd/bump-version <UPGRADE_NAME> --modules bank,wasmd,...
//	go run ./cmd/bump-version <UPGRADE_NAME> --all
//
// After running this, execute `go generate ./...` to regenerate setup.go files.
package main

import (
	"bufio"
	"fmt"
	"io"
	"os"
	"path/filepath"
	"regexp"
	"strings"

	"golang.org/x/mod/semver"
)

const (
	tagFile        = "app/tags"
	precompilesDir = "precompiles"
	commonDir      = "precompiles/common"
	modulePath     = "github.com/sei-protocol/sei-chain"
)

var (
	excludeDirs  = map[string]bool{"common": true, "utils": true}
	packageRe    = regexp.MustCompile(`^package\s+\S+`)
	commonImport = `"` + modulePath + `/precompiles/common"`
	generatedHdr = "// Code generated by cmd/bump-version; DO NOT EDIT."
)

func main() {
	args := parseArgs()

	// --- Validate and normalize the upgrade name ---
	// Upgrade names are vMajor.Minor (e.g. v7.0). Reject vMajor.Minor.Patch.
	// Normalize to vMajor.Minor.0 for storage.
	newTag := sanitizeUpgradeName(args.newTag)

	// --- Validate tag file exists ---
	if _, err := os.Stat(tagFile); os.IsNotExist(err) {
		fatalf("tag file not found: %s", tagFile)
	}

	// --- Load existing tags and check for conflicts ---
	existingTags, err := readNonEmptyLines(tagFile)
	if err != nil {
		fatalf("reading %s: %v", tagFile, err)
	}

	latestTag := findLatestSemver(existingTags)

	// Detect rerun: the tag is already the last line (from a previous run).
	// This must be checked before dedup mutates any files.
	rerun := len(existingTags) > 0 && existingTags[len(existingTags)-1] == newTag

	// Check if the tag (or a semver-equivalent like v6.4.0 for v6.4) already
	// exists anywhere in app/tags. The last-line exact match is a rerun.
	if pos, match := semverIndexOf(existingTags, newTag); pos != -1 {
		if pos == len(existingTags)-1 && match == newTag {
			// exact last-line match — this is a rerun, handled by dedup below
		} else {
			fatalf("%s (semver-equal to %s) already exists in %s at line %d", newTag, match, tagFile, pos+1)
		}
	}

	// Warn if the new tag sorts before the latest tag — this is almost certainly wrong.
	// On a rerun the tag IS the latest, so compare against the second-to-last.
	compareTarget := latestTag
	if rerun && len(existingTags) >= 2 {
		compareTarget = findLatestSemver(existingTags[:len(existingTags)-1])
	}
	if compareTarget != "" && !rerun && semver.Compare(newTag, compareTarget) < 0 {
		fatalf("%s is older than the current latest upgrade %s; upgrade names must advance forward", newTag, compareTarget)
	}

	if rerun {
		fmt.Printf("rerun detected: %s is already the last entry in %s, re-archiving\n", newTag, tagFile)
	}

	// Dedup on rerun: if NEW_TAG is already the last line in app/tags, remove it.
	dedup(tagFile, newTag)

	tagFolder := strings.ReplaceAll(newTag, ".", "")

	// --- Validate tag folder produces a legal Go package name ---
	if !isValidGoIdent(tagFolder) {
		fatalf("tag %q produces folder name %q which is not a valid Go identifier; use a clean vX.Y.Z tag", newTag, tagFolder)
	}

	legacyCommonImport := `"` + modulePath + `/precompiles/common/legacy/` + tagFolder + `"`

	// --- Resolve which modules to archive ---
	modules := args.modules
	if args.all {
		modules = discoverModules()
	}
	if len(modules) == 0 {
		fatalf("no modules to archive; use --modules or --all")
	}

	// --- Validate every requested module exists and has no conflicting legacy dir ---
	for _, mod := range modules {
		moduleDir := filepath.Join(precompilesDir, mod)
		if _, err := os.Stat(moduleDir); os.IsNotExist(err) {
			fatalf("module directory not found: %s", moduleDir)
		}
		legacyDir := filepath.Join(moduleDir, "legacy", tagFolder)
		if dirExists(legacyDir) && !rerun {
			fatalf("legacy directory %s already exists but %s is not the last entry in %s/versions — refusing to overwrite a previous archive", legacyDir, newTag, moduleDir)
		}
		// Check if the tag is already buried in the module's versions file
		if versions, _ := readNonEmptyLines(filepath.Join(moduleDir, "versions")); len(versions) > 0 {
			if pos, match := semverIndexOf(versions, newTag); pos != -1 && !(pos == len(versions)-1 && match == newTag) {
				fatalf("%s (semver-equal to %s) already exists in %s/versions at line %d", newTag, match, moduleDir, pos+1)
			}
		}
	}

	// Check common legacy dir conflict
	commonLegacyDir := filepath.Join(commonDir, "legacy", tagFolder)
	if dirExists(commonLegacyDir) && !rerun {
		fatalf("common legacy directory %s already exists — refusing to overwrite", commonLegacyDir)
	}

	// --- All checks passed, proceed ---
	fmt.Printf("new tag: %s\n", newTag)
	fmt.Printf("version folder: %s\n", tagFolder)
	fmt.Printf("latest existing: %s\n", latestTag)
	fmt.Printf("modules: %s\n", strings.Join(modules, ", "))
	fmt.Println()

	// Always archive common when any module is archived
	archiveCommon(tagFolder)

	// Archive each module
	for _, mod := range modules {
		moduleDir := filepath.Join(precompilesDir, mod)
		archiveModule(moduleDir, mod, newTag, tagFolder, legacyCommonImport)
	}

	// Append new tag to app/tags
	appendLine(tagFile, newTag)
	fmt.Printf("appended %s to %s\n", newTag, tagFile)

	fmt.Println("\nnext step: run `go generate ./...` to regenerate setup.go files")
}

// --- CLI parsing ---

type cliArgs struct {
	newTag  string
	modules []string
	all     bool
}

func parseArgs() cliArgs {
	args := os.Args[1:]
	if len(args) == 0 {
		usage()
	}

	result := cliArgs{newTag: args[0]}
	i := 1
	for i < len(args) {
		switch args[i] {
		case "--modules":
			i++
			if i >= len(args) {
				fatalf("--modules requires a comma-separated list")
			}
			for _, m := range strings.Split(args[i], ",") {
				m = strings.TrimSpace(m)
				if m != "" {
					result.modules = append(result.modules, m)
				}
			}
		case "--all":
			result.all = true
		default:
			fatalf("unknown argument: %s", args[i])
		}
		i++
	}

	if !result.all && len(result.modules) == 0 {
		usage()
	}
	return result
}

func usage() {
	fmt.Fprintf(os.Stderr, "usage: go run ./cmd/bump-version <NEW_TAG> --modules bank,wasmd,...\n")
	fmt.Fprintf(os.Stderr, "       go run ./cmd/bump-version <NEW_TAG> --all\n")
	os.Exit(1)
}

// --- Validation helpers ---

// vMajorMinorRe matches vMajor.Minor (e.g. v7.0, v12.3).
var vMajorMinorRe = regexp.MustCompile(`^v\d+\.\d+$`)

// vMajorMinorPatchRe matches vMajor.Minor.Patch (e.g. v7.0.0, v7.0.1).
var vMajorMinorPatchRe = regexp.MustCompile(`^v\d+\.\d+\.\d+$`)

// sanitizeUpgradeName validates and normalizes an upgrade name to vMajor.Minor.
//
// Accepted: vMajor.Minor (e.g. v7.0) — used as-is.
// Accepted with warning: vMajor.Minor.Patch (e.g. v7.0.1) — patch is dropped.
// Rejected: anything else.
func sanitizeUpgradeName(input string) string {
	if vMajorMinorRe.MatchString(input) {
		return input
	}
	if vMajorMinorPatchRe.MatchString(input) {
		normalized := semver.MajorMinor(input)
		fmt.Fprintf(os.Stderr, "WARNING: dropping patch from %s — upgrade names are vMajor.Minor; using %s\n", input, normalized)
		return normalized
	}
	fatalf("upgrade name %q is not valid; expected vMajor.Minor (e.g. v7.0, v7.1)", input)
	return "" // unreachable
}

// findLatestSemver returns the highest valid semver tag from the list,
// or "" if none are valid.
func findLatestSemver(tags []string) string {
	latest := ""
	for _, t := range tags {
		if semver.IsValid(t) && (latest == "" || semver.Compare(t, latest) > 0) {
			latest = t
		}
	}
	return latest
}

// semverIndexOf finds the first entry in lines that is semver-equal to value.
// Returns (position, matched_string) or (-1, "") if not found.
// This catches cross-format matches like v7.0 matching v7.0.0.
func semverIndexOf(lines []string, value string) (int, string) {
	for i, l := range lines {
		if semver.Compare(l, value) == 0 {
			return i, l
		}
	}
	return -1, ""
}

func dirExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && info.IsDir()
}

// isValidGoIdent checks that s is a legal Go identifier (letter/underscore
// start, then letters/digits/underscores).
func isValidGoIdent(s string) bool {
	if s == "" {
		return false
	}
	for i, r := range s {
		if i == 0 {
			if !isLetter(r) {
				return false
			}
		} else {
			if !isLetter(r) && !isDigit(r) {
				return false
			}
		}
	}
	return true
}

func isLetter(r rune) bool { return (r >= 'a' && r <= 'z') || (r >= 'A' && r <= 'Z') || r == '_' }
func isDigit(r rune) bool  { return r >= '0' && r <= '9' }

// --- Module discovery ---

func discoverModules() []string {
	entries, err := os.ReadDir(precompilesDir)
	if err != nil {
		fatalf("reading %s: %v", precompilesDir, err)
	}
	var modules []string
	for _, e := range entries {
		if e.IsDir() && !excludeDirs[e.Name()] {
			modules = append(modules, e.Name())
		}
	}
	return modules
}

// --- Archival ---

func archiveCommon(tagFolder string) {
	targetDir := filepath.Join(commonDir, "legacy", tagFolder)
	if err := os.MkdirAll(targetDir, 0755); err != nil {
		fatalf("creating %s: %v", targetDir, err)
	}

	for _, name := range []string{"precompiles.go", "evm_events.go"} {
		src := filepath.Join(commonDir, name)
		if _, err := os.Stat(src); os.IsNotExist(err) {
			continue
		}
		dst := filepath.Join(targetDir, name)
		if err := copyAndRewritePackage(src, dst, tagFolder); err != nil {
			fatalf("archiving %s: %v", src, err)
		}
	}

	fmt.Printf("archived common -> legacy/%s/\n", tagFolder)
}

func archiveModule(moduleDir, moduleName, newTag, tagFolder, legacyCommonImport string) {
	targetDir := filepath.Join(moduleDir, "legacy", tagFolder)
	if err := os.MkdirAll(targetDir, 0755); err != nil {
		fatalf("creating %s: %v", targetDir, err)
	}

	entries, err := os.ReadDir(moduleDir)
	if err != nil {
		fatalf("reading %s: %v", moduleDir, err)
	}

	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		name := e.Name()
		if !strings.HasSuffix(name, ".go") {
			continue
		}
		if strings.HasSuffix(name, "_test.go") || name == "setup.go" {
			continue
		}

		src := filepath.Join(moduleDir, name)
		dst := filepath.Join(targetDir, name)
		if err := copyRewritePackageAndImports(src, dst, tagFolder, legacyCommonImport); err != nil {
			fatalf("archiving %s: %v", src, err)
		}
	}

	// Copy abi.json if present
	abiSrc := filepath.Join(moduleDir, "abi.json")
	if _, err := os.Stat(abiSrc); err == nil {
		if err := copyFile(abiSrc, filepath.Join(targetDir, "abi.json")); err != nil {
			fatalf("copying abi.json for %s: %v", moduleName, err)
		}
	}

	// Update versions file
	versionsFile := filepath.Join(moduleDir, "versions")
	dedup(versionsFile, newTag)
	appendLine(versionsFile, newTag)

	fmt.Printf("archived %s -> legacy/%s/\n", moduleName, tagFolder)
}

// --- File rewriting ---

func copyAndRewritePackage(src, dst, newPkg string) error {
	content, err := os.ReadFile(src)
	if err != nil {
		return err
	}
	rewritten := packageRe.ReplaceAllString(string(content), generatedHdr+"\n\npackage "+newPkg)
	return os.WriteFile(dst, []byte(rewritten), 0644)
}

func copyRewritePackageAndImports(src, dst, newPkg, legacyCommonImport string) error {
	content, err := os.ReadFile(src)
	if err != nil {
		return err
	}
	rewritten := packageRe.ReplaceAllString(string(content), generatedHdr+"\n\npackage "+newPkg)
	rewritten = strings.ReplaceAll(rewritten, commonImport, legacyCommonImport)
	return os.WriteFile(dst, []byte(rewritten), 0644)
}

func copyFile(src, dst string) error {
	in, err := os.Open(src)
	if err != nil {
		return err
	}
	defer in.Close()

	out, err := os.Create(dst)
	if err != nil {
		return err
	}
	defer out.Close()

	_, err = io.Copy(out, in)
	return err
}

// --- File helpers ---

// dedup removes the last line of a file if it matches the given value.
// This allows safe reruns of the tool.
func dedup(path, value string) {
	if _, err := os.Stat(path); os.IsNotExist(err) {
		return
	}
	lines, err := readNonEmptyLines(path)
	if err != nil || len(lines) == 0 {
		return
	}
	if lines[len(lines)-1] == value {
		writeLines(path, lines[:len(lines)-1])
	}
}

func appendLine(path, line string) {
	f, err := os.OpenFile(path, os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0644)
	if err != nil {
		fatalf("opening %s: %v", path, err)
	}
	defer f.Close()
	fmt.Fprintln(f, line)
}

func readNonEmptyLines(path string) ([]string, error) {
	f, err := os.Open(path)
	if err != nil {
		return nil, err
	}
	defer f.Close()

	var lines []string
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line != "" {
			lines = append(lines, line)
		}
	}
	return lines, scanner.Err()
}

func writeLines(path string, lines []string) {
	content := strings.Join(lines, "\n") + "\n"
	if err := os.WriteFile(path, []byte(content), 0644); err != nil {
		fatalf("writing %s: %v", path, err)
	}
}

func fatalf(format string, args ...any) {
	fmt.Fprintf(os.Stderr, "bump-version: "+format+"\n", args...)
	os.Exit(1)
}
