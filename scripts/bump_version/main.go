// Precompile version management tool, triggered via go generate.
//
// When a new version is added to app/tags, this tool:
//  1. Detects which precompile modules changed (via git)
//  2. Archives their current code into legacy/ folders
//  3. Regenerates all setup.go files from versions files
//
// Upgrade names use vMajor.Minor format (e.g. v7.0, v7.1).
// If vMajor.Minor.Patch is found in app/tags, patch is dropped with a warning
// and the file is rewritten.
// Legacy folder/package names use vMajorMinor0 form (v7.0 -> v700).
//
// Usage: go generate ./...
package main

import (
	"bufio"
	"bytes"
	"embed"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"io"
	"log"
	"os"
	"path/filepath"
	"regexp"
	"strconv"
	"strings"
	"text/template"

	"github.com/go-git/go-git/v5"
	"github.com/go-git/go-git/v5/plumbing/object"
	"github.com/go-git/go-git/v5/plumbing/storer"
	"golang.org/x/mod/semver"
)

//go:embed setup.go.tmpl
var tmplFS embed.FS

const (
	tagFile        = "app/tags"
	precompilesDir = "precompiles"
	commonDir      = "precompiles/common"
	modulePath     = "github.com/sei-protocol/sei-chain"
	commonPkgPath  = modulePath + "/precompiles/common"
	generatedHdr   = "// Code generated by scripts/bump_version; DO NOT EDIT."
)

var (
	excludeDirs        = map[string]bool{"common": true, "utils": true}
	vMajorMinorRe      = regexp.MustCompile(`^v\d+\.\d+$`)
	vMajorMinorPatchRe = regexp.MustCompile(`^v\d+\.\d+\.\d+$`)
)

type LegacyVersion struct {
	Tag    string
	Folder string
}

type TemplateData struct {
	PackageName    string
	LegacyVersions []LegacyVersion
}

func main() {
	if err := run(); err != nil {
		log.Fatal(err)
	}
}

func run() error {
	// Phase 1: Read app/tags and detect new version
	tags, err := readNonEmptyLines(tagFile)
	if err != nil {
		return fmt.Errorf("reading %s: %w", tagFile, err)
	}
	if len(tags) == 0 {
		return fmt.Errorf("%s is empty", tagFile)
	}

	latestTag := tags[len(tags)-1]

	// Sanitize unconditionally so the tags file converges to vMajor.Minor form
	// even if a previous run crashed between rewrite and archival.
	sanitized, err := sanitizeUpgradeName(latestTag)
	if err != nil {
		return err
	}
	if sanitized != latestTag {
		tags[len(tags)-1] = sanitized
		if err := writeLines(tagFile, tags); err != nil {
			return err
		}
		fmt.Printf("rewrote %s in %s -> %s\n", latestTag, tagFile, sanitized)
		latestTag = sanitized
	}

	// Phase 2: Check if archiving is needed
	// versionFolder handles both vMajor.Minor and vMajor.Minor.Patch identically
	// (versionFolder("v6.4.0") == versionFolder("v6.4") == "v640").
	tagFolder := versionFolder(latestTag)
	commonLegacyDir := filepath.Join(commonDir, "legacy", tagFolder)

	if dirExists(commonLegacyDir) {
		fmt.Printf("version %s already archived, skipping to setup.go generation\n", latestTag)
	} else if err := archiveNewVersion(tags, latestTag, tagFolder); err != nil {
		return err
	}

	// Phase 3: Always regenerate all setup.go files
	return regenerateAllSetup()
}

func archiveNewVersion(tags []string, newTag, tagFolder string) error {
	fmt.Printf("new version detected: %s (folder: %s)\n", newTag, tagFolder)

	// Validate the new tag against prior entries
	if pos, match := semverIndexOf(tags[:len(tags)-1], newTag); pos != -1 {
		return fmt.Errorf("%s (semver-equal to %s) already exists in %s at line %d", newTag, match, tagFile, pos+1)
	}

	latestExisting := findLatestSemver(tags[:len(tags)-1])
	if latestExisting != "" && semver.Compare(newTag, latestExisting) < 0 {
		return fmt.Errorf("%s is older than the current latest upgrade %s; upgrade names must advance forward", newTag, latestExisting)
	}

	// Open the git repository and find the commit where the previous version
	// was added to app/tags. We diff precompiles/ against that commit.
	repo, err := git.PlainOpenWithOptions(".", &git.PlainOpenOptions{DetectDotGit: true})
	if err != nil {
		return fmt.Errorf("opening git repo: %w", err)
	}

	var baseCommit *object.Commit
	if latestExisting != "" {
		baseCommit, err = gitFindCommit(repo, latestExisting)
		if err != nil {
			return err
		}
	}

	legacyCommonPkgPath := modulePath + "/precompiles/common/legacy/" + tagFolder
	modules := discoverModules()

	// Detect which modules changed using git
	var changed []string
	if baseCommit == nil {
		// First version ever — no previous commit to diff against, archive everything
		changed = modules
	} else {
		fmt.Printf("comparing against %s (added %s)\n", baseCommit.Hash.String()[:12], latestExisting)
		commonChanged, err := gitPathHasChanges(repo, baseCommit, commonDir)
		if err != nil {
			return err
		}
		for _, mod := range modules {
			moduleDir := filepath.Join(precompilesDir, mod)
			modChanged, err := gitPathHasChanges(repo, baseCommit, moduleDir)
			if err != nil {
				return err
			}
			if commonChanged || modChanged {
				changed = append(changed, mod)
			}
		}
	}

	if len(changed) == 0 {
		fmt.Println("no precompile changes detected, skipping archival")
		return nil
	}

	fmt.Printf("changed modules: %s\n", strings.Join(changed, ", "))

	// Archive common whenever any module is archived; changed modules reference
	// the legacy common package so it must exist at the same legacy folder.
	if err := archiveCommon(tagFolder); err != nil {
		return err
	}

	for _, mod := range changed {
		moduleDir := filepath.Join(precompilesDir, mod)
		if err := archiveModule(moduleDir, mod, newTag, tagFolder, legacyCommonPkgPath); err != nil {
			return err
		}
	}

	fmt.Println()
	return nil
}

// gitFindCommit walks the commit log for app/tags and finds the commit that
// introduced the given version string. Walks newest to oldest; the introducing
// commit is the oldest one in the contiguous chain whose app/tags contains the
// version. PathFilter restricts the walk to commits that touch app/tags.
func gitFindCommit(repo *git.Repository, version string) (*object.Commit, error) {
	head, err := repo.Head()
	if err != nil {
		return nil, fmt.Errorf("could not get HEAD: %w", err)
	}

	iter, err := repo.Log(&git.LogOptions{
		From:       head.Hash(),
		PathFilter: func(path string) bool { return path == tagFile },
	})
	if err != nil {
		return nil, fmt.Errorf("git log failed: %w", err)
	}
	defer iter.Close()

	var result, prev *object.Commit
	_ = iter.ForEach(func(c *object.Commit) error {
		file, err := c.File(tagFile)
		if err != nil {
			if prev != nil {
				result = prev
			}
			return storer.ErrStop
		}
		content, err := file.Contents()
		if err != nil || !strings.Contains(content, version) {
			if prev != nil {
				result = prev
			}
			return storer.ErrStop
		}
		prev = c
		return nil
	})
	if result == nil {
		result = prev
	}
	if result == nil {
		return nil, fmt.Errorf("could not find commit that introduced %s in %s; ensure your precompile changes are committed", version, tagFile)
	}
	return result, nil
}

// gitPathHasChanges compares the HEAD tree against a base commit for a given
// directory prefix. Returns true if the subtree hash differs, which conservatively
// includes any change to any tracked file under that path (content, mode, or
// file addition/removal).
func gitPathHasChanges(repo *git.Repository, base *object.Commit, dirPath string) (bool, error) {
	baseTree, err := base.Tree()
	if err != nil {
		return false, fmt.Errorf("reading tree from base commit: %w", err)
	}

	baseSubtree, err := baseTree.Tree(dirPath)
	if err != nil {
		return true, nil // path didn't exist at base — new module, treat as changed
	}

	headRef, err := repo.Head()
	if err != nil {
		return false, fmt.Errorf("could not get HEAD: %w", err)
	}
	headCommit, err := repo.CommitObject(headRef.Hash())
	if err != nil {
		return false, fmt.Errorf("reading HEAD commit: %w", err)
	}
	headTree, err := headCommit.Tree()
	if err != nil {
		return false, fmt.Errorf("reading HEAD tree: %w", err)
	}
	headSubtree, err := headTree.Tree(dirPath)
	if err != nil {
		return true, nil // path doesn't exist at HEAD — removed module, treat as changed
	}

	return baseSubtree.Hash != headSubtree.Hash, nil
}

func archiveCommon(tagFolder string) error {
	targetDir := filepath.Join(commonDir, "legacy", tagFolder)
	if err := os.MkdirAll(targetDir, 0750); err != nil {
		return fmt.Errorf("creating %s: %w", targetDir, err)
	}

	for _, name := range []string{"precompiles.go", "evm_events.go"} {
		src := filepath.Join(commonDir, name)
		if _, err := os.Stat(src); os.IsNotExist(err) {
			continue
		}
		dst := filepath.Join(targetDir, name)
		if err := rewriteGoFile(src, dst, tagFolder, "", ""); err != nil {
			return fmt.Errorf("archiving %s: %w", src, err)
		}
	}

	fmt.Printf("archived common -> legacy/%s/\n", tagFolder)
	return nil
}

func archiveModule(moduleDir, moduleName, newTag, tagFolder, legacyCommonPkgPath string) error {
	targetDir := filepath.Join(moduleDir, "legacy", tagFolder)
	if err := os.MkdirAll(targetDir, 0750); err != nil {
		return fmt.Errorf("creating %s: %w", targetDir, err)
	}

	entries, err := os.ReadDir(moduleDir)
	if err != nil {
		return fmt.Errorf("reading %s: %w", moduleDir, err)
	}

	for _, e := range entries {
		if e.IsDir() {
			continue
		}
		name := e.Name()
		if !strings.HasSuffix(name, ".go") || strings.HasSuffix(name, "_test.go") || name == "setup.go" {
			continue
		}

		src := filepath.Join(moduleDir, name)
		dst := filepath.Join(targetDir, name)
		if err := rewriteGoFile(src, dst, tagFolder, commonPkgPath, legacyCommonPkgPath); err != nil {
			return fmt.Errorf("archiving %s: %w", src, err)
		}
	}

	abiSrc := filepath.Join(moduleDir, "abi.json")
	if _, err := os.Stat(abiSrc); err == nil {
		if err := copyFile(abiSrc, filepath.Join(targetDir, "abi.json")); err != nil {
			return fmt.Errorf("copying abi.json for %s: %w", moduleName, err)
		}
	}

	versionsFile := filepath.Join(moduleDir, "versions")
	existing, err := readNonEmptyLines(versionsFile)
	switch {
	case err == nil:
		if vErr := validateVersions(moduleName, existing); vErr != nil {
			return vErr
		}
		if pos, match := semverIndexOf(existing, newTag); pos != -1 {
			return fmt.Errorf("%s/versions: %s (semver-equal to %s) already exists at line %d", moduleName, newTag, match, pos+1)
		}
	case os.IsNotExist(err):
		// New module — versions file will be created by appendLine below.
	default:
		return fmt.Errorf("reading %s: %w", versionsFile, err)
	}
	if err := appendLine(versionsFile, newTag); err != nil {
		return err
	}

	fmt.Printf("archived %s -> legacy/%s/\n", moduleName, tagFolder)
	return nil
}

func regenerateAllSetup() error {
	tmpl, err := template.ParseFS(tmplFS, "setup.go.tmpl")
	if err != nil {
		return fmt.Errorf("parsing template: %w", err)
	}

	entries, err := os.ReadDir(precompilesDir)
	if err != nil {
		return fmt.Errorf("reading %s: %w", precompilesDir, err)
	}

	for _, entry := range entries {
		if !entry.IsDir() || excludeDirs[entry.Name()] {
			continue
		}

		moduleName := entry.Name()
		moduleDir := filepath.Join(precompilesDir, moduleName)

		versionsFile := filepath.Join(moduleDir, "versions")
		if _, err := os.Stat(versionsFile); os.IsNotExist(err) {
			continue
		}

		// Modules are expected to have a {moduleName}.go containing their
		// active code. If the convention isn't met, emit a clear warning
		// rather than silently skipping.
		if _, err := os.Stat(filepath.Join(moduleDir, moduleName+".go")); os.IsNotExist(err) {
			_, _ = fmt.Fprintf(os.Stderr, "skipping %s: expected %s/%s.go (convention: module dir contains {name}.go)\n", moduleName, moduleDir, moduleName)
			continue
		}

		versions, err := readNonEmptyLines(versionsFile)
		if err != nil {
			return fmt.Errorf("reading versions for %s: %w", moduleName, err)
		}
		if len(versions) == 0 {
			continue
		}
		if err := validateVersions(moduleName, versions); err != nil {
			return err
		}

		legacyCount := len(versions) - 1
		if legacyCount < 0 {
			legacyCount = 0
		}
		legacy := make([]LegacyVersion, 0, legacyCount)
		for _, v := range versions[:legacyCount] {
			legacy = append(legacy, LegacyVersion{
				Tag:    v,
				Folder: versionFolder(v),
			})
		}

		if err := generateSetup(tmpl, moduleDir, moduleName, legacy); err != nil {
			return fmt.Errorf("generating setup.go for %s: %w", moduleName, err)
		}

		fmt.Printf("generated %s/setup.go (%d legacy versions)\n", moduleDir, len(legacy))
	}
	return nil
}

func generateSetup(tmpl *template.Template, moduleDir, moduleName string, legacy []LegacyVersion) error {
	data := TemplateData{
		PackageName:    moduleName,
		LegacyVersions: legacy,
	}

	var buf strings.Builder
	if err := tmpl.Execute(&buf, data); err != nil {
		return fmt.Errorf("executing template: %w", err)
	}

	formatted, err := format.Source([]byte(buf.String()))
	if err != nil {
		return fmt.Errorf("gofmt: %w\nraw:\n%s", err, buf.String())
	}

	return os.WriteFile(filepath.Join(moduleDir, "setup.go"), formatted, 0600)
}

// sanitizeUpgradeName normalizes vMajor.Minor.Patch to vMajor.Minor and
// rejects anything that isn't one of those two forms.
func sanitizeUpgradeName(input string) (string, error) {
	if vMajorMinorRe.MatchString(input) {
		return input, nil
	}
	if vMajorMinorPatchRe.MatchString(input) {
		normalized := semver.MajorMinor(input)
		_, _ = fmt.Fprintf(os.Stderr, "WARNING: dropping patch from %s — upgrade names are vMajor.Minor; using %s\n", input, normalized)
		return normalized, nil
	}
	return "", fmt.Errorf("upgrade name %q in %s is not valid; expected vMajor.Minor (e.g. v7.0, v7.1)", input, tagFile)
}

func findLatestSemver(tags []string) string {
	latest := ""
	for _, t := range tags {
		if semver.IsValid(t) && (latest == "" || semver.Compare(t, latest) > 0) {
			latest = t
		}
	}
	return latest
}

func semverIndexOf(lines []string, value string) (int, string) {
	for i, l := range lines {
		if semver.Compare(l, value) == 0 {
			return i, l
		}
	}
	return -1, ""
}

// validateVersions ensures every entry in a module's versions file is a valid
// semver and that no two entries are semver-equal (e.g. v6.4 and v6.4.0).
func validateVersions(moduleName string, versions []string) error {
	for i, v := range versions {
		if !semver.IsValid(v) {
			return fmt.Errorf("%s/versions: line %d: %q is not a valid semver", moduleName, i+1, v)
		}
		if pos, match := semverIndexOf(versions[:i], v); pos != -1 {
			return fmt.Errorf("%s/versions: line %d: %q is semver-equal to line %d (%q)", moduleName, i+1, v, pos+1, match)
		}
	}
	return nil
}

// versionFolder converts a validated upgrade tag to its legacy folder name.
// Accepts vMajor.Minor or vMajor.Minor.Patch; two-part inputs are padded with
// a zero patch. Returns the dotless form: v7.0 -> v700, v6.4.0 -> v640.
// Panics on malformed input; callers must validate first.
func versionFolder(v string) string {
	switch {
	case vMajorMinorRe.MatchString(v):
		v += ".0"
	case vMajorMinorPatchRe.MatchString(v):
		// already in vMajor.Minor.Patch form
	default:
		panic(fmt.Sprintf("versionFolder: invalid tag %q (expected vMajor.Minor or vMajor.Minor.Patch)", v))
	}
	return strings.ReplaceAll(v, ".", "")
}

func dirExists(path string) bool {
	info, err := os.Stat(path)
	return err == nil && info.IsDir()
}

func discoverModules() []string {
	entries, err := os.ReadDir(precompilesDir)
	if err != nil {
		log.Fatalf("reading %s: %v", precompilesDir, err)
	}
	var modules []string
	for _, e := range entries {
		if e.IsDir() && !excludeDirs[e.Name()] {
			modules = append(modules, e.Name())
		}
	}
	return modules
}

// rewriteGoFile parses a Go source file, rewrites the package declaration to
// newPkg and (optionally) rewrites an import path from oldImport to newImport,
// then writes the result to dst with the generated-code header prepended.
// Using the AST instead of string replacement handles grouped imports, aliases,
// and renamed imports correctly.
//
// If oldImport is empty, no import rewriting is performed.
func rewriteGoFile(src, dst, newPkg, oldImport, newImport string) error {
	content, err := os.ReadFile(filepath.Clean(src))
	if err != nil {
		return err
	}

	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, src, content, parser.ParseComments)
	if err != nil {
		return fmt.Errorf("parsing %s: %w", src, err)
	}

	file.Name = ast.NewIdent(newPkg)

	if oldImport != "" {
		for _, imp := range file.Imports {
			path, err := strconv.Unquote(imp.Path.Value)
			if err != nil {
				continue
			}
			if path == oldImport {
				imp.Path.Value = strconv.Quote(newImport)
			}
		}
	}

	var buf bytes.Buffer
	if err := format.Node(&buf, fset, file); err != nil {
		return fmt.Errorf("formatting %s: %w", src, err)
	}

	// Prepend the generated-code header. format.Node emits the package clause
	// first; the header must precede it so tools that check the first line
	// (e.g. gopls, go generate scanners) see it.
	out := []byte(generatedHdr + "\n\n")
	out = append(out, buf.Bytes()...)

	return os.WriteFile(dst, out, 0600)
}

func copyFile(src, dst string) (err error) {
	in, err := os.Open(filepath.Clean(src))
	if err != nil {
		return err
	}
	defer func() { _ = in.Close() }()

	out, err := os.OpenFile(filepath.Clean(dst), os.O_WRONLY|os.O_CREATE|os.O_TRUNC, 0600)
	if err != nil {
		return err
	}
	defer func() {
		if cerr := out.Close(); cerr != nil && err == nil {
			err = cerr
		}
	}()

	_, err = io.Copy(out, in)
	return err
}

func appendLine(path, line string) error {
	f, err := os.OpenFile(filepath.Clean(path), os.O_APPEND|os.O_CREATE|os.O_WRONLY, 0600)
	if err != nil {
		return fmt.Errorf("opening %s: %w", path, err)
	}
	defer func() { _ = f.Close() }()
	if _, err := fmt.Fprintln(f, line); err != nil {
		return fmt.Errorf("appending %s: %w", path, err)
	}
	return nil
}

func readNonEmptyLines(path string) ([]string, error) {
	f, err := os.Open(filepath.Clean(path))
	if err != nil {
		return nil, err
	}
	defer func() { _ = f.Close() }()

	var lines []string
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line != "" {
			lines = append(lines, line)
		}
	}
	return lines, scanner.Err()
}

func writeLines(path string, lines []string) error {
	content := strings.Join(lines, "\n") + "\n"
	if err := os.WriteFile(path, []byte(content), 0600); err != nil {
		return fmt.Errorf("writing %s: %w", path, err)
	}
	return nil
}
