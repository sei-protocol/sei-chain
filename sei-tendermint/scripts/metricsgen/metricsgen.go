// metricsgen is a code generation tool for creating constructors for Tendermint
// metrics types.
package main

import (
	"bytes"
	"flag"
	"fmt"
	"go/ast"
	"go/format"
	"go/parser"
	"go/token"
	"go/types"
	"io"
	"io/fs"
	"log"
	"os"
	"path"
	"path/filepath"
	"reflect"
	"regexp"
	"strconv"
	"strings"
	"text/template"
	"unicode"
	"unicode/utf8"
)

func init() {
	flag.Usage = func() {
		fmt.Fprintf(os.Stderr, `Usage: %[1]s  -struct <struct>

Generate constructors for the metrics type specified by -struct contained in
the current directory. The tool creates a new file in the current directory
containing the generated code.

Options:
`, filepath.Base(os.Args[0]))
		flag.PrintDefaults()
	}
}

const (
	stdMetricsPackageName = "github.com/prometheus/client_golang/prometheus"
	intMetricsPackageName = "github.com/sei-protocol/sei-chain/sei-tendermint/libs/utils/prometheus"
)

const (
	metricNameTag = "metrics_name"
	labelsTag     = "metrics_labels"
	bucketTypeTag = "metrics_buckettype"
	bucketSizeTag = "metrics_bucketsizes"
	counterIntVec = "CounterIntVec"
	gaugeIntVec   = "GaugeIntVec"
)

var (
	dir   = flag.String("dir", ".", "Path to the directory containing the target package")
	strct = flag.String("struct", "Metrics", "Struct to parse for metrics")
)

var bucketType = map[string]string{
	"exprange": "prometheus.ExponentialBucketsRange",
	"exp":      "prometheus.ExponentialBuckets",
	"lin":      "prometheus.LinearBuckets",
	"none":     "tmprometheus.NoBuckets",
}

var tmpl = template.Must(template.New("tmpl").Parse(`// Code generated by metricsgen. DO NOT EDIT.

package {{ .Package }}

import (
	"github.com/prometheus/client_golang/prometheus"
{{- if .UsesIntMetrics }}
	tmprometheus "github.com/sei-protocol/sei-chain/sei-tendermint/libs/utils/prometheus"
{{- end }}
)

var Global = {{ .ConstructorName }}()

func init() {
	prometheus.MustRegister(
{{- range $metric := .ParsedMetrics }}
		Global.{{ $metric.FieldName }},
{{- end }}
	)
}

func {{ .ConstructorName }}() *{{ .StructName }} {
	return &{{ .StructName }}{
{{- range $metric := .ParsedMetrics }}
		{{ $metric.FieldName }}: {{ $metric.ConstructorPackage }}.{{ $metric.ConstructorName }}(prometheus.{{ $metric.OptsTypeName }}{
			Namespace: MetricsNamespace,
			Subsystem: MetricsSubsystem,
			Name:      "{{$metric.MetricName }}",
			Help:      "{{ $metric.Description }}",
{{- if ne $metric.HistogramOptions.BucketType "" }}
			Buckets: {{ $metric.HistogramOptions.BucketType }}({{ $metric.HistogramOptions.BucketSizes }}),
{{- else if ne $metric.HistogramOptions.BucketSizes "" }}
			Buckets: []float64{ {{ $metric.HistogramOptions.BucketSizes }} },
{{- end }}
		}, {{ if eq (len $metric.Labels) 0 }}nil{{ else }}[]string{ {{$metric.Labels}} }{{ end }}),
{{- end }}
	}
}

{{ range $metric := .ParsedMetrics }}
func (m *{{ $.StructName }}) {{ $metric.FieldName }}At({{ $metric.MethodParams }}) {{ $metric.MethodReturnType }} {
	return m.{{ $metric.FieldName }}.WithLabelValues({{ $metric.MethodArgs }})
}

{{ end }}
`))

// ParsedMetricField is the data parsed for a single field of a metric struct.
type ParsedMetricField struct {
	TypeName           string
	ConstructorPackage string
	ConstructorName    string
	OptsTypeName       string
	FieldName          string
	MetricName         string
	Description        string
	Labels             string
	LabelNames         []string

	MethodParams     string
	MethodArgs       string
	MethodReturnType string

	HistogramOptions HistogramOpts
}

type HistogramOpts struct {
	BucketType  string
	BucketSizes string
}

// TemplateData is all of the data required for rendering a metric file template.
type TemplateData struct {
	Package         string
	StructName      string
	ConstructorName string
	ParsedMetrics   []ParsedMetricField
	UsesIntMetrics  bool
}

func main() {
	flag.Parse()
	if *strct == "" {
		log.Fatal("You must specify a non-empty -struct")
	}
	td, err := ParseMetricsDir(".", *strct)
	if err != nil {
		log.Fatalf("Parsing file: %v", err)
	}
	out := filepath.Clean(filepath.Join(*dir, "metrics.gen.go"))
	f, err := os.Create(out)
	if err != nil {
		log.Fatalf("Opening file: %v", err)
	}
	defer func() { _ = f.Close() }()
	err = GenerateMetricsFile(f, td)
	if err != nil {
		log.Fatalf("Generating code: %v", err)
	}
}
func ignoreTestFiles(f fs.FileInfo) bool {
	return !strings.Contains(f.Name(), "_test.go")
}

// ParseMetricsDir parses the dir and scans for a struct matching structName,
// ignoring all test files. ParseMetricsDir iterates the fields of the metrics
// struct and builds a TemplateData using the data obtained from the abstract syntax tree.
func ParseMetricsDir(dir string, structName string) (TemplateData, error) {
	fs := token.NewFileSet()
	d, err := parser.ParseDir(fs, dir, ignoreTestFiles, parser.ParseComments)
	if err != nil {
		return TemplateData{}, err
	}
	if len(d) > 1 {
		return TemplateData{}, fmt.Errorf("multiple packages found in %s", dir)
	}
	if len(d) == 0 {
		return TemplateData{}, fmt.Errorf("no go pacakges found in %s", dir)
	}

	// Grab the package name and files.
	var (
		pkgName  string
		pkgFiles map[string]*ast.File
	)
	for name, parsedPkg := range d {
		pkgName = name
		pkgFiles = parsedPkg.Files
		break
	}
	td := TemplateData{
		Package:         pkgName,
		StructName:      structName,
		ConstructorName: constructorName(structName),
	}
	// Grab the metrics struct
	m, metricsPackageNames, err := findMetricsStruct(pkgFiles, structName)
	if err != nil {
		return TemplateData{}, err
	}
	for _, f := range m.Fields.List {
		if !isMetric(f.Type, metricsPackageNames) {
			continue
		}
		pmf := parseMetricField(f)
		if pmf.ConstructorPackage == "tmprometheus" || strings.HasPrefix(pmf.HistogramOptions.BucketType, "tmprometheus.") {
			td.UsesIntMetrics = true
		}
		td.ParsedMetrics = append(td.ParsedMetrics, pmf)
	}

	return td, err
}

func constructorName(structName string) string {
	if structName == "" {
		return "NewMetrics"
	}
	if isExported(structName) {
		return "New" + structName
	}
	r, size := utf8.DecodeRuneInString(structName)
	return "new" + string(unicode.ToUpper(r)) + structName[size:]
}

func isExported(name string) bool {
	r, _ := utf8.DecodeRuneInString(name)
	return unicode.IsUpper(r)
}

// GenerateMetricsFile executes the metrics file template, writing the result
// into the io.Writer.
func GenerateMetricsFile(w io.Writer, td TemplateData) error {
	b := []byte{}
	buf := bytes.NewBuffer(b)
	err := tmpl.Execute(buf, td)
	if err != nil {
		return err
	}
	b, err = format.Source(buf.Bytes())
	if err != nil {
		return err
	}
	_, err = io.Copy(w, bytes.NewBuffer(b))
	if err != nil {
		return err
	}
	return nil
}

func findMetricsStruct(files map[string]*ast.File, structName string) (*ast.StructType, map[string]struct{}, error) {
	var (
		st *ast.StructType
	)
	for _, file := range files {
		metricsPackageNames, err := extractMetricsPackageNames(file.Imports)
		if err != nil {
			return nil, nil, fmt.Errorf("unable to determine metrics package name: %v", err)
		}
		if !ast.FilterFile(file, func(name string) bool {
			return name == structName
		}) {
			continue
		}
		ast.Inspect(file, func(n ast.Node) bool {
			switch f := n.(type) {
			case *ast.TypeSpec:
				if f.Name.Name == structName {
					var ok bool
					st, ok = f.Type.(*ast.StructType)
					if !ok {
						err = fmt.Errorf("found identifier for %q of wrong type", structName)
					}
				}
				return false
			default:
				return true
			}
		})
		if err != nil {
			return nil, nil, err
		}
		if st != nil {
			return st, metricsPackageNames, nil
		}
	}
	return nil, nil, fmt.Errorf("target struct %q not found in dir", structName)
}

func parseMetricField(f *ast.Field) ParsedMetricField {
	typeName := extractTypeName(f.Type)
	pmf := ParsedMetricField{
		Description:        extractHelpMessage(f.Doc),
		MetricName:         extractFieldName(f.Names[0].String(), f.Tag),
		FieldName:          f.Names[0].String(),
		TypeName:           typeName,
		ConstructorPackage: extractConstructorPackage(typeName),
		ConstructorName:    "New" + typeName,
		OptsTypeName:       extractOptsTypeName(typeName),
		LabelNames:         extractLabelNames(f.Tag),
		MethodReturnType:   extractMethodReturnType(typeName),
	}
	pmf.Labels = joinQuotedLabels(pmf.LabelNames)
	pmf.MethodParams = buildMethodParams(pmf.LabelNames)
	pmf.MethodArgs = buildMethodArgs(pmf.LabelNames)
	if pmf.OptsTypeName == "HistogramOpts" {
		pmf.HistogramOptions = extractHistogramOptions(f.Tag)
	}
	return pmf
}

func extractTypeName(e ast.Expr) string {
	return strings.TrimPrefix(path.Ext(types.ExprString(e)), ".")
}

func extractConstructorPackage(typeName string) string {
	switch typeName {
	case counterIntVec, gaugeIntVec:
		return "tmprometheus"
	default:
		return "prometheus"
	}
}

func extractOptsTypeName(typeName string) string {
	switch typeName {
	case counterIntVec:
		return "CounterOpts"
	case gaugeIntVec:
		return "GaugeOpts"
	default:
		return strings.TrimSuffix(typeName, "Vec") + "Opts"
	}
}

func extractMethodReturnType(typeName string) string {
	switch typeName {
	case "CounterVec":
		return "prometheus.Counter"
	case counterIntVec:
		return "*tmprometheus.CounterInt"
	case "GaugeVec":
		return "prometheus.Gauge"
	case gaugeIntVec:
		return "*tmprometheus.GaugeInt"
	case "HistogramVec":
		return "prometheus.Observer"
	default:
		return ""
	}
}

func extractHelpMessage(cg *ast.CommentGroup) string {
	if cg == nil {
		return ""
	}
	var help []string //nolint: prealloc
	for _, c := range cg.List {
		mt := strings.TrimPrefix(c.Text, "//metrics:")
		if mt != c.Text {
			return strings.TrimSpace(mt)
		}
		help = append(help, strings.TrimSpace(strings.TrimPrefix(c.Text, "//")))
	}
	return strings.Join(help, " ")
}

func isMetric(e ast.Expr, metricsPackageNames map[string]struct{}) bool {
	expr := types.ExprString(e)
	for packageName := range metricsPackageNames {
		if strings.Contains(expr, fmt.Sprintf("%s.", packageName)) {
			return true
		}
	}
	return false
}

func extractLabelNames(bl *ast.BasicLit) []string {
	if bl != nil {
		t := reflect.StructTag(strings.Trim(bl.Value, "`"))
		if v := t.Get(labelsTag); v != "" {
			parts := strings.Split(v, ",")
			res := make([]string, 0, len(parts))
			for _, s := range parts {
				res = append(res, strings.TrimSpace(s))
			}
			return res
		}
	}
	return nil
}

func joinQuotedLabels(labels []string) string {
	if len(labels) == 0 {
		return ""
	}
	quoted := make([]string, 0, len(labels))
	for _, label := range labels {
		quoted = append(quoted, strconv.Quote(label))
	}
	return strings.Join(quoted, ",")
}

func buildMethodParams(labels []string) string {
	if len(labels) == 0 {
		return ""
	}
	params := make([]string, 0, len(labels))
	for _, label := range labels {
		params = append(params, fmt.Sprintf("%s string", labelToParamName(label)))
	}
	return strings.Join(params, ", ")
}

func buildMethodArgs(labels []string) string {
	if len(labels) == 0 {
		return ""
	}
	args := make([]string, 0, len(labels))
	for _, label := range labels {
		args = append(args, labelToParamName(label))
	}
	return strings.Join(args, ", ")
}

func labelToParamName(label string) string {
	name := nonIdentifierChar.ReplaceAllString(label, "_")
	name = strings.Trim(name, "_")
	if name == "" {
		name = "label"
	}
	firstRune, _ := utf8.DecodeRuneInString(name)
	if unicode.IsDigit(firstRune) {
		name = "_" + name
	}
	if _, isKeyword := goKeywords[name]; isKeyword {
		name += "Label"
	}
	return name
}

func extractFieldName(name string, tag *ast.BasicLit) string {
	if tag != nil {
		t := reflect.StructTag(strings.Trim(tag.Value, "`"))
		if v := t.Get(metricNameTag); v != "" {
			return v
		}
	}
	return toSnakeCase(name)
}

func extractHistogramOptions(tag *ast.BasicLit) HistogramOpts {
	h := HistogramOpts{}
	if tag != nil {
		t := reflect.StructTag(strings.Trim(tag.Value, "`"))
		if v := t.Get(bucketTypeTag); v != "" {
			h.BucketType = bucketType[v]
		}
		if v := t.Get(bucketSizeTag); v != "" {
			h.BucketSizes = v
		}
	}
	return h
}

func extractMetricsPackageNames(imports []*ast.ImportSpec) (map[string]struct{}, error) {
	names := make(map[string]struct{}, 2)
	for _, i := range imports {
		u, err := strconv.Unquote(i.Path.Value)
		if err != nil {
			return nil, err
		}
		if u != stdMetricsPackageName && u != intMetricsPackageName {
			continue
		}
		if i.Name != nil {
			names[i.Name.Name] = struct{}{}
			continue
		}
		names[path.Base(u)] = struct{}{}
	}
	return names, nil
}

var capitalChange = regexp.MustCompile("([a-z0-9])([A-Z])")
var nonIdentifierChar = regexp.MustCompile(`[^a-zA-Z0-9_]`)

var goKeywords = map[string]struct{}{
	"break": {}, "default": {}, "func": {}, "interface": {}, "select": {},
	"case": {}, "defer": {}, "go": {}, "map": {}, "struct": {},
	"chan": {}, "else": {}, "goto": {}, "package": {}, "switch": {},
	"const": {}, "fallthrough": {}, "if": {}, "range": {}, "type": {},
	"continue": {}, "for": {}, "import": {}, "return": {}, "var": {},
}

func toSnakeCase(str string) string {
	snake := capitalChange.ReplaceAllString(str, "${1}_${2}")
	return strings.ToLower(snake)
}
